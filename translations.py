def translate(key, lang='pt'):
    """
    Traduz uma chave para o idioma especificado.
    
    Args:
        key (str): Chave de tradução
        lang (str): Código do idioma ('pt' ou 'es')
    
    Returns:
        str: Texto traduzido ou a chave se não encontrada
    """
    if key in TRANSLATIONS:
        return TRANSLATIONS[key].get(lang, TRANSLATIONS[key]['pt'])
    return key

def get_all_translations(lang='pt'):
    """
    Retorna todas as traduções para um idioma específico.
    
    Args:
        lang (str): Código do idioma ('pt' ou 'es')
    
    Returns:
        dict: Dicionário com todas as traduções
    """
    return {key: value.get(lang, value['pt']) for key, value in TRANSLATIONS.items()}
